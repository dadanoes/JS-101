// Operator Asignment

// Asigment dasar ( = )
let a = 10;
console.log(a, typeof a); // 10 'number'

// Asigment penjumlahan ( += )
let b = 10;
b += 5; // 10 + 5 = 15
console.log(b, typeof b); // 15 'number'

// Asigment pengurangan ( -= )
let c = 10;
c -= 5; // 10 - 5 = 5
console.log(c, typeof c); // 5 'number'

// Asigment perkalian ( *= )
let d = 10;
d *= 5; // 10 * 5 = 50
console.log(d, typeof d); // 50 'number'

// Asigment pembagian ( /= )
let e = 10;
e /= 5; // 10 / 5 = 2
console.log(e, typeof e); // 2 'number'

// Asigment perpangkatan ( **= )
let f = 10;
f **= 5; // 10 ** 5 = 100000
console.log(f, typeof f); // 100000 'number'

// modulus (%=)
let g = 10;
g %= 3; // Sama dengan g = g % 3; sekarang g adalah 1 (sisa bagi)
console.log(g, typeof g); // 1 'number'
