// Operator Logic

// && (Logika AND) (menghasilkan nilai true jika kedua variable bernilai true)
let a = true;
let b = true;
console.log(a && b); //true karena dua duanya bernilai true

// || (Logika OR) (menghasilkan nilai true jika salah satu variable bernilai true)
let c = true;
let d = false;
console.log(c || d); //true karena c bernilai true

// ! (Logika NOT) (menghasilkan nilai true jika variable bernilai false) (kebalikan dari nilai)
let e = false;
console.log(!e); //false karena e bernilai true
